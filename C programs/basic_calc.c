#include<stdio.h>
int main(void){
    printf("Please enter 2 numbers\n");
    int a,b;
    scanf("%d",&a);
    scanf("%d",&b);
    printf("The sum of the numbers is\n");
    printf("%d", a+b);
    printf("\n");
    printf("The difference of the numbers is\n");
    printf("%d", a-b);
    printf("\n");
    printf("The product of the numbers is\n");
    printf("%d", a*b);
    printf("\n");
    printf("The quotient of the numbers is\n");
    printf("%d", a/b);
    printf("\n");
}